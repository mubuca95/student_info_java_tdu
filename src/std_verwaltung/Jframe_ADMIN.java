    public void refresh_table(){
        DBconnect connect = new DBconnect();
        Vector<LVA> v = connect.get_all_lva();
         
        Dozent d = null;
        GRUPPE g = null;
        DefaultTableModel model = (DefaultTableModel)jTable_list_lva1.getModel();
        model.getDataVector().removeAllElements();
        for(int c = 0; c < v.size();c++){
            d = connect.get_DozentData(v.elementAt(c).getDozent_ID());
            g = connect.get_gruppe(v.elementAt(c).getGruppe_id());
            model.addRow(new Object[]{v.elementAt(c).getLVAname(),v.elementAt(c).getLVAid(),g.get_gruppe_name(),d.getDozentName()+" "+d.getDozentNachname()});
        } 
        model = (DefaultTableModel)jTable_list_lva.getModel();
        model.getDataVector().removeAllElements();
        for(int c = 0; c < v.size();c++){
            d = connect.get_DozentData(v.elementAt(c).getDozent_ID());
            g = connect.get_gruppe(v.elementAt(c).getGruppe_id());
            model.addRow(new Object[]{v.elementAt(c).getLVAname(),v.elementAt(c).getLVAid(),g.get_gruppe_name(),d.getDozentName()+" "+d.getDozentNachname()});
         }      
    }
   
