    public void fill_combobox(){  // Erfüllen der Comoboxes
        jComboBox_dozent_name.removeAllItems();
        jComboBox_gruppe_auswahl.removeAllItems();
        DBconnect connect = new DBconnect();
        Vector<Dozent> v = connect.get_all_DozentData();
        Vector<GRUPPE> v2 = connect.get_all_gruppe();
        for(int c = 0; c < v.size() ; c++){
            jComboBox_dozent_name.addItem(v.elementAt(c).getDozentName()+" "+v.elementAt(c).getDozentNachname()+":"+v.elementAt(c).getDozent_ID());  
        }
        for(int c = 0; c < v2.size() ; c++){
            jComboBox_gruppe_auswahl.addItem(v2.elementAt(c).get_gruppe_name()+":"+v2.elementAt(c).get_gruppe_id());
        }
    }
